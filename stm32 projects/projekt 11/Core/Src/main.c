/* USER CODE BEGIN 0 */
void MAG_Init(void)
{
    uint8_t data;

    // 🔹 CRA_REG_M (0x00)
    // 0x14 = 30 Hz
    data = 0x14;
    HAL_I2C_Mem_Write(&hi2c1, MAG_ADDR, 0x00, 1, &data, 1, HAL_MAX_DELAY);

    // 🔹 CRB_REG_M (0x01)
    // 0x20 = gain (±1.3 Gauss)
    data = 0x20;
    HAL_I2C_Mem_Write(&hi2c1, MAG_ADDR, 0x01, 1, &data, 1, HAL_MAX_DELAY);

    // 🔹 MR_REG_M (0x02)
    // 0x00 = continuous mode
    data = 0x00;
    HAL_I2C_Mem_Write(&hi2c1, MAG_ADDR, 0x02, 1, &data, 1, HAL_MAX_DELAY);
}
void ACC_Init(void)
{
    uint8_t data;

    // 🔹 CTRL_REG1 (0x20)
    // 0x57 = 100 Hz, wszystkie osie włączone
    data = 0x57;
    HAL_I2C_Mem_Write(&hi2c1, ACC_ADDR, 0x20, 1, &data, 1, HAL_MAX_DELAY);

    // 🔹 CTRL_REG4 (0x23)
    // 0x08 = High Resolution, ±2g
    data = 0x08;
    HAL_I2C_Mem_Write(&hi2c1, ACC_ADDR, 0x23, 1, &data, 1, HAL_MAX_DELAY);
}
