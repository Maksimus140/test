/**
  * @brief  The application entry point.
  * @retval int
  */
volatile int16_t ax, ay, az;
void acc_read(void)
{
	uint8_t data[6];
	HAL_I2C_Mem_Read(&hi2c1, ACC_ADDR, 0X28 | 0X80, 1, data, 6, HAL_MAX_DELAY);
	ax = data[0] | (data[1] << 8);
	ay = data[2] | (data[3] << 8);
	az = data[4] | (data[5] << 8);
}
volatile int16_t ax_m, ay_m, az_m;
void mag_read(void)
{
	uint8_t mag_data[6];
	HAL_I2C_Mem_Read(&hi2c1, MAG_ADDR, 0x03, 1, mag_data, 6, HAL_MAX_DELAY);
	ax_m = (mag_data[0] << 8) | mag_data[1];
	az_m = (mag_data[2] << 8) | mag_data[3];
	ay_m = (mag_data[4] << 8) | mag_data[5];
}

